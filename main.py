@bot.message_handler(commands=['start'])
def send_welcome(message):
    reply_markup = telebot.types.ReplyKeyboardMarkup(resize_keyboard=True)
    quote_lenin_button = telebot.types.KeyboardButton('Пацанские цитаты')
    quote_stalin_button = telebot.types.KeyboardButton('Реальные цитаты')
    reply_markup.add(quote_lenin_button, quote_stalin_button)
    bot.send_message(message.chat.id, "Привет! Выбери цитату, которую хочешь прочитать:", reply_markup=reply_markup)

@bot.message_handler(func=lambda message: True)
def send_quote(message):
    if message.text == 'Пацанские цитаты':
        quote = random.choice(lenin_quotes)
        bot.send_message(message.chat.id, quote)
    elif message.text == 'Реальные цитаты':
        quote = random.choice(stalin_quotes)
        bot.send_message(message.chat.id, quote)
    else:
        bot.send_message(message.chat.id, "Выберите одну из предложенных кнопок.")

